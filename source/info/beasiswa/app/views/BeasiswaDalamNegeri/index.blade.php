</p><p><strong>Beasiswa Dalam Negeri</strong> yaitu beasiswa untuk 
pelajar Indonesia dan mahasiswa perguruan tinggi negeri/swasta di 
Indonesia jenjang diploma, sarjana, dan pasca sarjana. Beasiswa ini 
diberikan oleh pemerintah RI melalui kementerian pendidikan dan 
kebudayaan, juga lembaga-lembaga donatur beasiswa dari perusahaan BUMN 
dan swasta nasional yang peduli pada dunia pendidikan.</p>

<p><a rel="nofollow" class="external" href="http://dikti.go.id/" target="_blank"><strong>Beasiswa Dosen</strong></a><br>
Direktorat Jenderal Pendidikan Tinggi Kementerian Pendidikan dan 
Kebudayaan RI memberikan tawaran beasiswa dalam negeri S2 dan S3 untuk 
dosen PTN/PTS dan umum.</p>

