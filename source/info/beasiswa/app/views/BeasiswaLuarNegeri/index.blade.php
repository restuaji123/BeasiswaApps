     </p><p><strong>Beasiswa Luar Negeri</strong> adalah beasiswa yang 
diberikan kepada mahasiswa Indonesia untuk melanjutkan studi ke luar 
negeri. Beasiswa ini diberikan oleh pemerintah Indonesia dan sponsor 
lokal lainnya atau diberikan oleh lembaga internasional dan perguruan 
tinggi di negara yang dituju. Penerima beasiswa ini akan menerima 
manfaat beasiswa seperti biaya kuliah <em>free</em>, biaya buku dan 
perlengkapan studi, ongkos tiket dan visa, bantuan biaya hidup dan sewa 
tempat tinggal, biaya penelitian, bahkan di beberapa negara mahasiswa 
asing mendapatkan asuransi kesehatan.</p>

<p><a rel="nofollow" class="external" href="http://beasiswa.dikti.go.id/" target="_blank"><strong>Beasiswa Luar Negeri dari Dikti</strong></a><br>
Direktorat Jenderal Pendidikan Tinggi Kementerian Pendidikan dan 
Kebudayaan RI memberikan tawaran beasiswa ke luar negeri untuk dosen dan
 umum. Misalnya <a href="http://www.infobeasiswa.net/beasiswa-lpdp-magister-dan-doktor/">beasiswa LPDP</a> dari Kemenkeu, <a href="http://www.infobeasiswa.net/indonesian-german-scholarship-program-igsp/">beasiswa IGSP</a> dari Jerman.</p>

<p>
Beasiswa Asia: Arab Saudi, Rusia, Cina, Jepang, Korea Selatan, India, Pakistan, Malaysia, Singapura, Brunei Darussalam, Thailand<br>
Beasiswa Eropa: Jerman, Italia, Perancis, Belanda, Spanyol, Turki, Finlandia, Belgia, Swiss, Inggris<br>
Beasiswa Amerika, Kanada, Mesir, Afrika Selatan, Australia, Selandia Baru.
</p>

